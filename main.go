package main

import (
	"fmt"
	"math"
	"os"
	"strconv"

	ini "github.com/gookit/ini"
	colorful "github.com/lucasb-eyer/go-colorful"
)

// named color in hcl space
type nc struct {
	name string
	li   float64
	ch   float64
	hu   float64
}

var (
	// h float64
	// c float64
	// l float64

	// define a slice of structs containig main hues as go-colorful colours
	clrs = []nc{
		{"red", 0.0, 1.0, 0.5},
		{"orange", 0.0, 1.0, 0.5},
		{"yellow", 0.0, 1.0, 0.5},
		{"lime", 0.0, 1.0, 0.5},
		{"green", 0.0, 1.0, 0.5},
		{"teal", 0.0, 1.0, 0.5},
		{"cyan", 0.0, 1.0, 0.5},
		{"blue", 0.0, 1.0, 0.5},
		{"indigo", 0.0, 1.0, 0.5},
		{"violet", 0.0, 1.0, 0.5},
		{"fuschia", 0.0, 1.0, 0.5},
		{"pink", 0.0, 1.0, 0.5},
		{"grey", 0.0, 1.0, 0.5},
	}

	variants = []nc{}
)

func main() {

	// Load configuration from file
	config, err := ini.LoadExists("palegen.ini")
	if err != nil {
		panic(err)
	}

	// Read base colour from config
	base, ok := config.String("hex")
	if ok {
		fmt.Println("Base color found: hex", base)
	}
	// input color as CSS style hex
	ic := base

	// convert input from hex to colorful.color
	hc, err := colorful.Hex(ic)
	if err != nil {
		fmt.Printf("Error: %v\n", err)
		return
	}

	// colorful.Color converted to LCHuv space (luminosity, chroma, hue)
	l, c, h := hc.LuvLCh()

	fmt.Println("Input color:", ic)
	fmt.Println("Converted to LCHuv space:", l, c, h)

	// generate colors through 12 hue steps
	rotateHue(l, c, h)

	//generate a grey tinted with input (base) color
	grey(l, c, h)

	// generate 10 lightness level variants of each hue
	genVariants(l, c, h)

	// Output file with clrs as css vars
	// Get output destination
	outputFile, ok := config.String("outputFile")
	if !ok {
		outputFile = "colors.css"
	}
	// Open outputFile for writing
	if f, err := os.Create(outputFile); err != nil {
		fmt.Printf("Error creating file: %v\n", err)
	} else {
		// defers close file to when function finishes running
		defer f.Close()

		// start root section of css
		if _, err = f.WriteString(`:root {
/* Generated by palegen */
`); err != nil {
			fmt.Printf("Error: %v\n", err)
		}

		// Write the input base color directly as css vars
		// base as hex
		// base := colorful.Hcl(h, c, l).Clamped().Hex()
		// _, err = fmt.Fprintf(f, "  --base: %s;\n", base)
		// if err != nil {
		// 	fmt.Printf("Error: %v\n", err)
		// }

		// base as hsla
		h, s, l := colorful.LuvLCh(l, c, h).HSLuv()
		_, err = fmt.Fprintf(f, "  --base: hsla(%.2f, %.2f%%, %.2f%%, 1);\n", h, float64(s*100), float64(l*100))
		if err != nil {
			fmt.Printf("Error: %v\n", err)
		}

		// convert clrs slice to CSS output as css vars
		for i := range clrs {
			h, s, l := colorful.LuvLCh(clrs[i].li, clrs[i].ch, clrs[i].hu).HSLuv()
			_, err = fmt.Fprintf(f, "  --%s: hsla(%.2f, %.2f%%, %.2f%%, 1);\n", clrs[i].name, h, float64(s*100), float64(l*100))
			if err != nil {
				fmt.Printf("Error: %v\n", err)
			}
		}

		// blank line in css
		_, err = fmt.Fprintf(f, "\n")
		if err != nil {
			fmt.Printf("Error: %v\n", err)
		}

		// Directly Add a black tinted to background via alpha
		_, err = f.WriteString("  --textblack: hsla(0, 100%, 0%, 0.9);\n")
		if err != nil {
			fmt.Printf("Error: %v\n", err)
		}

		// Directly Add a wite tinted to background via alpha
		_, err = f.WriteString("  --textwhite: hsla(360, 100%, 100%, 0.9);\n")
		if err != nil {
			fmt.Printf("Error: %v\n", err)
		}

		// empty line
		_, err = f.WriteString("\n")
		if err != nil {
			fmt.Printf("Error: %v\n", err)
		}

		// Print lightness variants as CSS
		for i := range variants {
			h, s, _ := colorful.LuvLCh(variants[i].li, variants[i].ch, variants[i].hu).HSLuv()
			fmt.Println(variants[i].ch, s)
			_, err = fmt.Fprintf(f, "  --%s: hsla(%.2f, %.2f%%, %.2f%%, 1);\n", variants[i].name, h, float64(s*100), variants[i].li)
			if err != nil {
				fmt.Printf("Error: %v\n", err)
			}
		}

		// end root section of css
		_, err = f.WriteString("\n}")
		if err != nil {
			fmt.Printf("Error: %v\n", err)
		}

		fmt.Println("Color generation complete.")
	}
}

func rotateHue(l float64, c float64, h float64) []nc {

	// rotate the hue value around the full 360 degress with 12 steps
	steps := 12
	hueStep := float64(360 / steps)

	for i := 0; i < steps; i++ {

		h := math.Mod(((float64(i) * hueStep) + h), 360)

		switch {
		case h >= 0 && h <= 10:
			for i := range clrs {
				if clrs[i].name == "pink" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h >= 10 && h <= 40:
			for i := range clrs {
				if clrs[i].name == "red" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 40 && h <= 70:
			for i := range clrs {
				if clrs[i].name == "orange" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 70 && h <= 100:
			for i := range clrs {
				if clrs[i].name == "yellow" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 100 && h <= 130:
			for i := range clrs {
				if clrs[i].name == "lime" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 130 && h <= 160:
			for i := range clrs {
				if clrs[i].name == "green" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 160 && h <= 190:
			for i := range clrs {
				if clrs[i].name == "teal" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 190 && h <= 220:
			for i := range clrs {
				if clrs[i].name == "cyan" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 220 && h <= 250:
			for i := range clrs {
				if clrs[i].name == "blue" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 250 && h <= 280:
			for i := range clrs {
				if clrs[i].name == "indigo" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 280 && h <= 310:
			for i := range clrs {
				if clrs[i].name == "violet" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 310 && h <= 340:
			for i := range clrs {
				if clrs[i].name == "fuschia" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		case h > 340 && h <= 360:
			for i := range clrs {
				if clrs[i].name == "pink" {
					clrs[i].li = l
					clrs[i].ch = c
					clrs[i].hu = math.Floor(h)
				}
			}
		}
	}
	return clrs
}

func grey(l float64, c float64, h float64) []nc {

	nc := c / 10

	for i := range clrs {
		if clrs[i].name == "grey" {
			clrs[i].li = 0.5
			clrs[i].ch = nc
			clrs[i].hu = math.Floor(h)
		}
	}
	return clrs
}

func genVariants(l float64, c float64, h float64) []nc {

	// step through fixed lightness values
	lightness := [10]float64{98, 88, 78, 67, 57, 47, 37, 26, 16, 6}

	for x := range clrs {
		for i := 0; i < len(lightness); i++ {
			nnc := new(nc)
			nnc.name = clrs[x].name + strconv.Itoa(i)
			nnc.li = lightness[i]
			nnc.ch = clrs[x].ch
			nnc.hu = clrs[x].hu
			variants = append(variants, *nnc)
		}
	}
	return variants

}
