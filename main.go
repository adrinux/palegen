func rotateHue(h float64, c float64, l float64) []nc {

	// rotate the hue value around the full 360 degress with 12 steps
	steps := 12
	size := float64(360 / steps)

	for i := 0; i < steps; i++ {
		if (h + size) <= 360 {
			h = h + size
		} else if (h + size) > 360 {
			h = (h + size) - 360
		}
		//fmt.Printf("h is: %v \n", h)

		switch {
		case h >= 0 && h <= 30:
			for i := range clrs {
				if clrs[i].name == "red" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 30 && h <= 60:
			for i := range clrs {
				if clrs[i].name == "orange" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 60 && h <= 90:
			for i := range clrs {
				if clrs[i].name == "yellow" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 90 && h <= 120:
			for i := range clrs {
				if clrs[i].name == "lime" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 120 && h <= 150:
			for i := range clrs {
				if clrs[i].name == "green" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 150 && h <= 180:
			for i := range clrs {
				if clrs[i].name == "teal" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 180 && h <= 210:
			for i := range clrs {
				if clrs[i].name == "cyan" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 210 && h <= 240:
			for i := range clrs {
				if clrs[i].name == "blue" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 240 && h <= 270:
			for i := range clrs {
				if clrs[i].name == "indigo" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 270 && h <= 300:
			for i := range clrs {
				if clrs[i].name == "violet" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 300 && h <= 330:
			for i := range clrs {
				if clrs[i].name == "fuschia" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		case h > 330 && h <= 360:
			for i := range clrs {
				if clrs[i].name == "pink" {
					clrs[i].hu = h
					clrs[i].ch = c
					clrs[i].li = l
				}
			}
		}
	}
	return clrs
}

